# Output through printf with newline in Bash
# The printf keyword lets you print something in the console, but it has the ability to include new lines through \n
# Example:
printf "Multiple line command\nThis is cool!"
# The output will be like this:
# Multiple line command
# This is cool!
# The other methods doesn't support new lines.
# You can't do this with a common Windows PowerShell. You need Bash.
